def mapearCarpetas(od): 
  nombres=np.array(od['names'])
  id= np.array(od['id'], dtype=object)
  folders=np.array(od['folders'])
  objeto=np.array(od['id'], dtype=object)

  for i in range (len(nombres)):
    objeto[i]=(folders[i], nombres[i])

  for i in range(len(id)):
    for j in range(len(id[i])):
        if objeto[i][0]==id[i][j]:
           id[i][j]=objeto[i][1] 

        for g in range(len(objeto)):
           if objeto[g][0]==id[i][j]:
            id[i][j]=objeto[g][1] 

       #print(id[i][j])
       # print(i,j)

  for i in range(len(id)):
      id[i].insert(0,folders[i])


